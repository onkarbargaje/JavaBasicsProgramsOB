package sDET;

public class Swaping2Numbers 
{

	public static void main(String[] args)
	{
		int a=20;
		int b=10;
		System.out.println("values before swaping are "+ a +" and " + b);
		
		int t; // by assigning temp variable
		t=a;
		a=b;
		b=t;
		System.out.println("values after swaping are "+ a +" and " + b);

	}

}
